package dblab.tinkeredmst.InputProcessers;

public class Point {
    private int xCoor;
    private int yCoor;

    public Point(int xCoor, int yCoor) {
        this.xCoor = xCoor;
        this.yCoor = yCoor;
    }

    public int getxCoor() {
        return xCoor;
    }

    public int getyCoor() {
        return yCoor;
    }
}
